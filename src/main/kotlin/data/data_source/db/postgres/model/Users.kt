    val post_default_public = bool("post_default_public")
    val consumed_timestep = integer("consumed_timestep")
    val otp_required_for_login = bool("otp_required_for_login")
    val otp_backup_codes = text("otp_backup_codes")
    val plain_otp_secret = varchar("plain_otp_secret", 100)
